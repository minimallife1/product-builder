export function getRecommendations({ ageMonths, gender, developmentalStage }) {
  let filtered = recommendationsData.filter(item => {
    const ageMatch = item.ageMin <= ageMonths && ageMonths <= item.ageMax;
    const genderMatch = item.gender === null || item.gender === gender || gender === ''; // null means applicable to all, empty string means user didn't specify
    const stageMatch = item.developmentalStage === developmentalStage || developmentalStage === ''; // empty string means user didn't specify
    return ageMatch && genderMatch && stageMatch;
  });

  // Simple sorting for now, can be expanded with popularity scores if needed
  // For now, it will return items as they appear in the data for simplicity,
  // effectively making the order in recommendationsData as the "priority".
  return filtered.slice(0, 5);
}
